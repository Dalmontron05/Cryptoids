package Cryptoids.Creatures;

public class Cryptoid {
    // Attributes
    private String name;
    private String type;
    private int attack;
    private int hp;
    private String art;

    public Cryptoid(String name, String type, int attack, int hp, String art)
    {
        this.name = name; 
        this.type = type; 
        this.attack = attack; 
        this.hp = hp; 
        this.art = art; 
    }
}

